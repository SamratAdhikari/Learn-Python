# For Assertion:


for x in range(1,100):
    print(x)

count = 0
for x in range(1,4):
    count = count + x

print(count)



def cross(vape):
    count = 0
    for x in vape:
        count = count + x


    return count


assert cross([1,2,3])





print(cross([1,6]))



