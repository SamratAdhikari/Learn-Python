# Number System

# Dec - Bin
print(bin(69))
print()

# Bin - Dec -- Write '0b' in front
print(0b1000101)
print()

# Dec - Oct
print(oct(69))
print()

# Oct - Dec -- Write '0o' in front
print(0o105)
print()

# Dec - Hex
print(hex(69))
print()

# Hex - Dec -- Write '0x' in front
print(0x45)
print()